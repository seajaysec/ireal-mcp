/**
 * Best-effort split of a raw progression string into measures (for previews of
 * raw input and for decoded charts). Strips layout symbols and returns the
 * chord-ish tokens per measure.
 */
export function splitProgressionToMeasures(progression: string): { chords: string[] }[] {
  // Remove staff text blocks.
  let p = progression.replace(/<[^>]*>/g, " ");
  // Split on any barline.
  const rawMeasures = p.split(/[\[\]{}|Z]+/);

  // Structural single-letter tokens to drop entirely.
  const STRUCTURAL = new Set(["s", "l", "S", "Q", "f", "Y", "U"]);

  const measures: { chords: string[] }[] = [];
  for (const raw of rawMeasures) {
    let body = raw;
    // Strip multi-char structural tokens (section marks, time sigs, endings).
    body = body.replace(/\*[ABCDVi]/g, " ");
    body = body.replace(/T\d\d/g, " ");
    body = body.replace(/N\d/g, " ");

    // Tokens separated by spaces or commas.
    const tokens = body
      .split(/[\s,]+/)
      .map((t) => t.trim())
      .filter((t) => t.length > 0 && !STRUCTURAL.has(t));

    const chords: string[] = [];
    for (let t of tokens) {
      // Strip a leading size marker attached to a chord (e.g. "sEh", "lD-").
      t = t.replace(/^[sl](?=[A-G])/, "");
      if (t === "n") chords.push("N.C.");
      else if (t === "x") chords.push("%");
      else if (t === "r") chords.push("%%");
      else if (t === "p") chords.push("/");
      else if (t.length > 0) chords.push(t);
    }

    if (chords.length > 0) measures.push({ chords });
  }
  return measures;
}
