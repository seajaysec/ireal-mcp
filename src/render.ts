interface IndexChart {
  slug: string;
  title: string;
  composer?: string;
  style?: string;
  key?: string;
  variant?: "straight" | "embellished";
  savedAt?: string;
  asciiPreview?: string;
  irealbUrl?: string;
}

function variantBadge(variant?: string): string {
  if (!variant) return "";
  return ` <span class="badge badge-${variant}">${variant}</span>`;
}

function metaLine(c: IndexChart): string {
  return [c.composer, c.style, c.key ? `Key ${c.key}` : ""]
    .filter((x): x is string => Boolean(x))
    .map(htmlEscape)
    .join(" · ");
}

function whenLabel(savedAt?: string): string {
  if (!savedAt) return "";
  // Show the date portion (YYYY-MM-DD) — enough to sort/scan by.
  return savedAt.slice(0, 10);
}

/**
 * The library index page. The most recently updated chart is featured at the
 * top (with preview + import); every chart is listed below, newest first.
 * `charts` is expected to be pre-sorted by recency (newest first).
 */
export function indexHtml(charts: IndexChart[], opts: { playlistHref?: string } = {}): string {
  const featured = charts[0];

  const featuredBlock = featured
    ? `  <div class="featured">
    <div class="meta">Most recent${featured.savedAt ? ` · updated ${whenLabel(featured.savedAt)}` : ""}</div>
    <h2><a href="/chart/${encodeURIComponent(featured.slug)}">${htmlEscape(featured.title)}</a>${variantBadge(featured.variant)}</h2>
    <div class="meta">${metaLine(featured)}</div>
    ${featured.irealbUrl ? `<p><a class="import" href="/import/${encodeURIComponent(featured.slug)}">Open in iReal Pro &raquo;</a></p>` : ""}
    ${featured.asciiPreview ? `<pre class="preview">${htmlEscape(featured.asciiPreview)}</pre>` : ""}
  </div>`
    : "";

  const rows = charts.length
    ? charts
        .map((c, i) => {
          const tag = i === 0 ? ` <span class="meta">(latest)</span>` : "";
          const when = c.savedAt ? `<span class="meta"> · ${whenLabel(c.savedAt)}</span>` : "";
          return `    <li>
      <a href="/chart/${encodeURIComponent(c.slug)}"><strong>${htmlEscape(c.title)}</strong></a>${variantBadge(c.variant)}${tag}
      &nbsp; <a href="/import/${encodeURIComponent(c.slug)}">import &raquo;</a>
      <div class="meta">${metaLine(c)}${when}</div>
    </li>`;
        })
        .join("\n")
    : `    <li class="meta">No charts yet. Create one with the create_chart tool.</li>`;

  const playlist =
    charts.length && opts.playlistHref
      ? `  <p><a class="import" href="${opts.playlistHref}">Open all ${charts.length} as one iReal Pro playlist &raquo;</a></p>`
      : "";

  return `<!DOCTYPE html>
<html>
<head>
  <meta http-equiv="Content-Type" content="text/html; charset=utf-8">
  <meta name="viewport" content="width=device-width, initial-scale=1">
  <title>iReal Pro charts</title>
  <style type="text/css">${PAGE_STYLE}</style>
</head>
<body>
  <div class="topbar"><h1>iReal Pro charts</h1></div>
${featuredBlock}
${playlist}
  <h3>All charts (${charts.length}) <span class="meta">— newest first</span></h3>
  <ul class="charts">
${rows}
  </ul>
  <p class="info">Served by ireal-mcp. Tap import links on a device with iReal Pro installed.</p>
</body>
</html>
`;
}

/** Render the harmonic analysis as an HTML section. */
export function analysisHtml(analysis: AnalysisLike | undefined): string {
  if (!analysis) return "";

  const planItems = analysis.jamPlan.map((p) => `      <li>${htmlEscape(p)}</li>`).join("\n");
  const cells = analysis.cells.length
    ? `    <p class="meta">Cadences &amp; cells</p>
    <ul class="cells">
${analysis.cells.map((c) => `      <li>${htmlEscape(c)}</li>`).join("\n")}
    </ul>`
    : "";

  const rows = analysis.chords
    .map(
      (c) => `      <tr${c.scaleChange ? ' class="borrowed"' : ""}>
        <td><strong>${htmlEscape(c.symbol)}</strong></td>
        <td>${htmlEscape(c.roman)}</td>
        <td>${htmlEscape(c.function)}</td>
        <td>${c.scaleChange ? "<strong>scale change → </strong>" : ""}${htmlEscape(c.scale)}<div class="meta mono">${htmlEscape(c.scaleNotes)}</div>${c.note ? `<div class="meta">${htmlEscape(c.note)}</div>` : ""}</td>
      </tr>`,
    )
    .join("\n");

  const penta = analysis.homePentatonic
    ? `<br>Shortcut: ${htmlEscape(analysis.homePentatonic)} — <span class="mono">${htmlEscape(analysis.homePentatonicNotes ?? "")}</span>`
    : "";

  return `  <h3>Analysis &amp; jam plan</h3>
  <div class="onescale">One scale for the whole tune: <strong>${htmlEscape(analysis.homeScale)}</strong> — <span class="mono">${htmlEscape(analysis.homeScaleNotes)}</span>${penta}</div>
  <ul class="plan">
${planItems}
  </ul>
${cells}
  <table class="analysis">
    <thead><tr><th>Chord</th><th>Roman</th><th>Function</th><th>Scale to play</th></tr></thead>
    <tbody>
${rows}
    </tbody>
  </table>
`;
}

interface AnalysisLike {
  homeKeyLabel?: string;
  homeScale: string;
  homeScaleNotes: string;
  homePentatonic?: string;
  homePentatonicNotes?: string;
  jamPlan: string[];
  cells: string[];
  chords: {
    symbol: string;
    roman: string;
    function: string;
    scale: string;
    scaleNotes: string;
    borrowed: boolean;
    scaleChange?: boolean;
    note?: string;
  }[];
}

